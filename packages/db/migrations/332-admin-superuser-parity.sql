merged_permissions as (
  select
    ar.role_id,
    jsonb_agg(d.permission order by d.permission) as permissions
  from admin_roles ar
  cross join lateral (
    select existing.permission
    from jsonb_array_elements_text(coalesce(ar.permissions, '[]'::jsonb)) as existing(permission)
    union
    select gp.permission
    from granted_permissions gp
  ) d
  group by ar.role_id
)
update public.roles r
set permissions = mp.permissions
from merged_permissions mp
where r.id = mp.role_id
  and r.code in ('admin', 'org.access.admin', 'org.platform.admin');
